#切片操作
lst=[10,20,30,40,50,60,70,80]

print(lst[1:7:2]) #start=1,stop=7,step=2,数字指的是索引，输出[20,40,60]
print(lst[:7:2]) #start不写默认从头开始,10,30,50,70
print(lst[1::2])#stop不写默认执行到底，20,40,60,80
#step不写默认为1

print('-------------------step为负数情况--------------------------')
print('原列表',lst)
print(lst[::-1])#step为负数，会逆序
print(lst[6::-1])#不写stop默认向左执行到底  70，60，50，40，30，20，10
print(lst[:1:-2])#不写start默认从最右边开始运行  80，60，40